	private ForbiddenMaterialsUtil(Material material)
	{
		this.material = material;
	}
	
	private Material getMaterial()
	{
		return this.material;
	}
	
	public static boolean isForbiddenMaterial(Material material)
	{
		return ((Stream.of(ForbiddenMaterialsUtil.values()).filter(m -> m.getMaterial().equals(material)).count() == 0) ? false : true);
	}
	
